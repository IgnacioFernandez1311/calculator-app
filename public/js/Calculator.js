class Calculator {
    add(number1, number2) {
        return number1 + number2;
    }

    substract(number1, number2) {
        return number1 - number2;
    }

    divide(number1, number2) {
        return number1 / number2;
    }

    times(number1, number2) {
        return number1 * number2;
    }
}